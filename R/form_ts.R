format.timeSpan <- 
  list("387d 3h 24m 32s 241MS" = 
       list(output = "%dd %Hh %Mm %Ss %NMS",
	    input = "%d %w %H %w %M %w %S %w %N %w" ),
       "387 days 3 hours 24 minutes" =
       list(output = "%d days %H hours %M minutes",
	    input = "%d %w %H %w %M %w" ),
       "1y 22d 3h 24m 32s 241MS" =
       list(output = "%yy %Dd %Hh %Mm %Ss %NMS",
	    input = "%y %w %d %w %H %w %M %w %S %w %N %w" ),
       "55 weeks, 2 days, 3 hours" =
       list(output = "%W weeks, %E days, %H hours",
	    input = "%W %w %d %w %H %w" ))


